_start:

	//控制GPL2_0为输出，把相应的寄存器写入对应的值，r1为寄存器地址，r0为写入的数据
	ldr r0, =0x00000001
	ldr r1, =0x11000100
	str  r0, [r1]            //将r0中的值写入到r1中区去
	
	//写对用的数据寄存器，控制哪个LED灯亮
	//ldr r0, =0x10000000     //不可以点亮LED	
	ldr r0, =0x00000001     //可以点亮LED
	ldr r1, =0x11000104
	str  r0, [r1]
	
	
	//控制GPIO为输出，把相应的寄存器写入对应的值，GPK1_1
	ldr r0, =0x00000010		  //配置为输出模式
	ldr r1, =0x11000060		  //GPK1CON基地址
	str  r0, [r1]             //将r0中的值写入到r1中区去
	
	//写对用的数据寄存器，控制哪个LED灯亮
	ldr r0, =0x2
	ldr r1, =0x11000064
	str  r0, [r1]
	
	
flag:
	b flag   //b跳转指令，跳转到flag,形成死循环
	
